package com.iplaza.fito_beta;

public class Item {
    private String ScientificName;
    private String PopularName;
    private String Location;
    private String Description;
    private String MedicalProperties;
    private String NutritionProperties;
    private String Formula;
    private String Combinations;
    private String Cultivate;
    private String Season;
    private String UsableParts;

    public String getScientificName() {
        return ScientificName;
    }

    public void setScientificName(String scientificName) {
        ScientificName = scientificName;
    }

    public String getPopularName() {
        return PopularName;
    }

    public void setPopularName(String popularName) {
        PopularName = popularName;
    }

    public String getLocation() {
        return Location;
    }

    public void setLocation(String location) {
        Location = location;
    }

    public String getDescription() {
        return Description;
    }

    public void setDescription(String description) {
        Description = description;
    }

    public String getMedicalProperties() {
        return MedicalProperties;
    }

    public void setMedicalProperties(String medicalProperties) {
        MedicalProperties = medicalProperties;
    }

    public String getNutritionProperties() {
        return NutritionProperties;
    }

    public void setNutritionProperties(String nutritionProperties) {
        NutritionProperties = nutritionProperties;
    }

    public String getFormula() {
        return Formula;
    }

    public void setFormula(String formula) {
        Formula = formula;
    }

    public String getCombinations() {
        return Combinations;
    }

    public void setCombinations(String combinations) {
        Combinations = combinations;
    }

    public String getCultivate() {
        return Cultivate;
    }

    public void setCultivate(String cultivate) {
        Cultivate = cultivate;
    }

    public String getSeason() {
        return Season;
    }

    public void setSeason(String season) {
        Season = season;
    }

    public String getUsableParts() {
        return UsableParts;
    }

    public void setUsableParts(String usableParts) {
        UsableParts = usableParts;
    }
}
